Employee 表：
+-------------+------+
| Column Name | Type |
+-------------+------+
| id          | int  |
| salary      | int  |
+-------------+------+
id 是这个表的主键。
表的每一行包含员工的工资信息。
 

编写一个 SQL 查询，获取并返回 Employee 表中第二高的薪水 。如果不存在第二高的薪水，查询应该返回 null 。

select max(salary) from Employee where salary<( select max(salary) from Employee);

select salary as SecondHighestSalary from Employee where salary<( select max(salary) from Employee) order by salary desc limit 1;

select max(e1.salary) as SecondHighestSalary
from Employee e1 left join Employee e2 on e1.salary<e2.salary
where e2.id is not null;

SELECT
    IFNULL(
      (SELECT DISTINCT Salary
       FROM Employee
       ORDER BY Salary DESC
        LIMIT 1 OFFSET 1),
    NULL) AS SecondHighestSalary





